class Currency: Equatable {
    var code: String
    var country: String
    var flag: String

    init(code: String, country: String, flag: String) {
        self.code = code
        self.country = country
        self.flag = flag
    }
    
    static func == (lhs: Currency, rhs: Currency) -> Bool {
        return lhs.code == rhs.code && lhs.country == rhs.country && lhs.flag == rhs.flag
    }
}
